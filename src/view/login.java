    private void selesaiNewPeminjamanButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_selesaiNewPeminjamanButtonActionPerformed
       int kembalian = 0;
        try{
            if(idMemberPinjamTextField.getText().equals("") && idDvdPinjamTextField.getText().equals("")){
               errorPinjamLabel.setText("Proses gagal. Terdapat text field yang kosong");
               errorPinjamLabel.setForeground(Color.RED);
            }
            else{
                int idMember = Integer.parseInt(idMemberPinjamTextField.getText());
                int idDvd = Integer.parseInt(idDvdPinjamTextField.getText());
                int bayar = Integer.parseInt(bayarPinjamTextField.getText());
                 if(memberModel.isLagiPinjam(idMember)){
                     JOptionPane.showMessageDialog(rootPane, "Tidak dapat membuat transaksi baru. Member ini masih dalam peminjaman.", null, JOptionPane.ERROR_MESSAGE);
                 }
                 if(dvdModel.loadDataStok(idDvd)== 0){
                     JOptionPane.showMessageDialog(rootPane, "Tidak ada lagi stok untuk DVD ini.", null, JOptionPane.ERROR_MESSAGE);
                 }
                 else{ //benar
                     if(bayar < 3000){
                         JOptionPane.showMessageDialog(rootPane, "Pembayaran kurang dari harga sewa DVD.", null, JOptionPane.ERROR_MESSAGE);
                     }
                     else if(bayar >= 3000){
                        kembalian = bayar - 3000;
                        transaksiController.tambahTransaksi(idMember, idDvd);
                        memberModel.updateLagiPinjam(idMember, true);
                        dvdModel.updateStok(idDvd, dvdModel.loadDataStok(idDvd)-1);
                        JOptionPane.showMessageDialog(rootPane, "Transaksi berhasil ditambahkan\nKembalian: Rp. "+kembalian, null, JOptionPane.INFORMATION_MESSAGE);
                     }
                }
            }
       }
       catch(NumberFormatException nfe) {JOptionPane.showMessageDialog(rootPane, "Input tidak valid", null, JOptionPane.ERROR_MESSAGE);}
       catch(Exception e){JOptionPane.showMessageDialog(rootPane, "Terdapat kesalahan saat mengakses database", null, JOptionPane.ERROR_MESSAGE);}
    }//GEN-LAST:event_selesaiNewPeminjamanButtonActionPerformed

    private void pengembalianButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_pengembalianButtonActionPerformed
        long denda = 0;
        try{
            int idTransaksi = Integer.parseInt(idTransaksiKembaliTextField.getText());
            transaksiModel.loadData(idTransaksi);
            if(idTransaksiKembaliTextField.getText()==""){
                errorPengembalianLabel.setText("Proses gagal. Terdapat text field yang kosong");
                errorPengembalianLabel.setForeground(Color.RED);
            }
            else if(transaksiModel.isSudahDikembalikan()==false){
                //terakhir edit disiinni
                Date tglKembali = transaksiModel.getTglKembali();
                Date tglDikembalikan = new Date();
                
                if(tglDikembalikan.after(tglKembali)){
                    long selisih  = tglDikembalikan.getTime() - tglKembali.getTime();
                    long nHariTelat = TimeUnit.MILLISECONDS.toDays(selisih);
                    denda = 2000*nHariTelat;
                }
                transaksiModel.updateSudahDikembalikan(true);
                memberModel.updateLagiPinjam(transaksiModel.getIdMemberPeminjam(), false); //set member lagi meminjam = false
                dvdModel.updateStok(transaksiModel.getIdDvdDipinjam(), dvdModel.loadDataStok(transaksiModel.getIdDvdDipinjam())+1); //set stok dvd yg dipinjam = +1
                JOptionPane.showMessageDialog(rootPane, "Proses berhasil\n Denda: Rp "+ denda, null, JOptionPane.INFORMATION_MESSAGE);
            }
            else{
                JOptionPane.showMessageDialog(rootPane, "Transaksi ini sudah selesai. Proses tidak dapat dilanjutkan", null, JOptionPane.ERROR_MESSAGE);
            }
        }
        catch(NumberFormatException nfe) {
            errorPengembalianLabel.setText("Input tidak valid");
            errorPengembalianLabel.setForeground(Color.RED);
        }
       catch(Exception e){JOptionPane.showMessageDialog(rootPane, "Terdapat kesalahan saat mengakses database", null, JOptionPane.ERROR_MESSAGE);}
    }//GEN-LAST:event_pengembalianButtonActionPerformed

